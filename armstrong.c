#include<stdio.h>
void main()
{
	int num,armstrong=0,reminder,n;
	printf("enter the number");
	scanf("%d",&num); 
	n=num;
	while(n>0)
	{
		reminder=n%10;
		armstrong=armstrong +(reminder*reminder*reminder);
		n=n/10;
	}
		if(num==armstrong)
				printf("%d is armstrong number",num);
		else
				printf("%d is not armstrong number",num);
}
